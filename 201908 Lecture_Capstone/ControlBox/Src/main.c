/* USER CODE BEGIN 0 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if(htim == &htim6) //timer6
  {    
    //control period time check
    tenmilli++;
    if(tenmilli % 10 == 0)
    {
      second++;
    }
    
    //incremental encoder
    EncoderCounter = TIM1->CNT;
    if(cal_Absolute(pre_EncoderCounter - EncoderCounter) > 10000)
    {
      EncoderCounter = 0;
    }
    Direction = __HAL_TIM_IS_TIM_COUNTING_DOWN(&htim1);
    dis = EncoderCounter * 0.011;//(7.0/300.0);

    //absolute encoder
    ABSposition = (rx_data[0] - 48) * 1000 + (rx_data[1] - 48) * 100 + (rx_data[2] - 48) * 10 + (rx_data[3] - 48);
    deg = ABSposition * 0.087890;
    n_deg = (int)deg;
    
    //control part
    ///////////////////////////////////////////////////////////////////////////////
    //ddis = dis - pre_dis;
    if(dis > 50)
    {
      if(init_flag == 0)
      {
        x0 = dis * cos(deg * 0.0174533);
        y0 = dis * sin(deg * 0.0174533);
        init_flag = 1;
      }
      
      Ex = x0 - dis * cos(deg * 0.0174533);
      Ey = y0 - dis * sin(deg * 0.0174533);
      
      Vx = -0.4* Ex; //-  0.05 * Exsum;
      Vy = -0.4* Ey; //-  0.05 * Eysum;
      
      left = (Vx * 0.121212  - Vy * 0.0679059) * 9.549297;
      right = (Vx * 0.121212 + Vy * 0.0679059) * 9.549297;
      
      L = left;
      R = right;
      
      //Saturation and Direction 
      if(left < 0)
      {
        left = - left;
        id1_Ctrl_Vel[6] = DIR_LEFT_MINUS;
        if(left > 100){ left = 100; }
        else
        {
          Exsum = Exsum + Ex;
          Eysum = Eysum + Ey;
        }
      }
      else
      {
        id1_Ctrl_Vel[6] = DIR_LEFT_PLUS;
        if(left > 100){ left = 100; }
        else
        {
          Exsum = Exsum + Ex;
          Eysum = Eysum + Ey;
        }
      }
      if(right < 0)
      {
        right = - right;
        id0_Ctrl_Vel[6] = DIR_RIGHT_MINUS;
        if(right > 100){ right = 100; }
        else
        {
         Exsum = Exsum + Ex;
          Eysum = Eysum + Ey;
        }
      }
      else
      {
        id0_Ctrl_Vel[6] = DIR_RIGHT_PLUS;
        if(right > 100){ right = 100; }
        else
        {
          Exsum = Exsum + Ex;
          Eysum = Eysum + Ey;
        }
      }
      id0_rpm = (int)right*10 ;
      id1_rpm = (int)left*10;
    }
    else
    {
      id0_rpm = 0;
      id1_rpm = 0;
    }
    
      //////////////////////////////////////////////////////////////////////////
    //id0_rpm = 200;
    //id1_rpm = 200;
    id0_Ctrl_Vel[7] = id0_rpm >> 8;
    id0_Ctrl_Vel[8] = (unsigned char)id0_rpm;
    id1_Ctrl_Vel[7] = id1_rpm >> 8;
    id1_Ctrl_Vel[8] = (unsigned char)id1_rpm;
    cal_CheckSum(id0_Ctrl_Vel);
    cal_CheckSum(id1_Ctrl_Vel);

    pre_dis = dis;
    motor_flag = 1;
  
  }
  pre_EncoderCounter = EncoderCounter;
  
}

